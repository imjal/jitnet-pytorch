def visualize_masks(labels):
    def convert2dtorch(arr): # convert a one-hot into a thresholded array
        max_arr = arr.max(axis=0)
        new_arr = arr.argmax(axis = 0) + 1
        new_arr[max_arr.values < 0.1] = 0 # set low confidence to background
        return new_arr
    labels = convert2dtorch(labels[0].detach()).cpu().numpy()
    labels_vis = np.zeros((labels.shape[0], labels.shape[1], 3), np.uint8)
    for i in range(labels.shape[0]):
      for j in range(labels.shape[1]):
        labels_vis[i, j] = color_list[labels[i, j]]
    return labels_vis